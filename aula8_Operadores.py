# Nessa aula o professor passou sobre operadores e a maioria já sabia. Vou marcar abaixo o que achei interessante

# Divisão
divisao = 10 / 3
print('Divisão', divisao)

# Divisão Inteira
divisao_inteira = 10 // 3
print('Divisão Inteira', divisao_inteira)

# Exponenciação
exponenciacao = 10 ** 3
print('Exponenciação', exponenciacao)

# Módulo (o resto da divisão)
modulo = 10 % 3 
print('Módulo', modulo)

# Para verificar se um número é PAR basta fazer um módulo por 2. Se o resto for zero, significa que ele é par. Caso contrário é impar.
print(5 % 2)
print(8 % 2)
print(11 % 2)
print(14 % 2)

# Eu posso tbm simplificar com boolean
print(5 % 2 == 0)
print(8 % 2 == 0)
print(11 % 2 == 0)
print(14 % 2 == 0)

# Basicamente TRUE é par e FALSE é impar. 
