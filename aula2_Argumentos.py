#Argumentos nomeados (usa-se o sep para adicionar algum elemento de separação ex.:12+34)
print(12,34,"Arom", sep='+')

#CRLF Cariage Return + Line Feed também expressado por /r/n <<(é o caracter de quebra de linha do windows)

#o end adiciona um elemento no final
print(12,34,"Arom", sep='-', end='%')

